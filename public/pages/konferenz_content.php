        <h1>Bits &amp; Bäume Conference 2022: 30.09.-02.10.2022 </h1>

      
        <section id="cfp">

            <h2>Call for Participation</h2>

            <p>

            <h3>Bits & Bäume 2022 – The Conference for Digitalisation and Sustainability</h3>
            </p>
                
            <p>
            We connect communities and stand up for political change in the interaction of digitalisation 
and sustainability. Support the movement with your contribution to the conference!
            </p>

            <p>
            After the successful Bits & Bäume Conference in 2018 the topics of digitalisation and 
sustainability are more present than ever within our society. For the purpose of achieving a fair 
and sustainable design of our world as well as our environment we are facing big societal and 
global challenges. That‘s why we want to stabilse and extend the collaboration between 
environmental- and social justice activists, technical experts and human rights activists via the 
conference Bits & Bäume 2022 which will take place from September 30th until October 2nd. 
In order to find common solutions for the pressing questions of our time one of our main goals 
is to initiate networking opportunities for communities and representatives of socially and 
ecologically oriented small companies, start-ups and founders. We want to discuss major 
political strategies and governance proposals, but also learn from each other through best 
practice examples on how to achieve climate and environmental protection, social justice, 
democracy and fundamental rights in our digital age. </p>

            <p>Together in panel discussions, workshops and open spaces we want to reflect on how to take 
action and how to specifically implement sustainable digitalisation. 
</p>

            <p><b>Our key questions are:</b></p>

            <ul>
                <li>1.  How should digitalisation be shaped in a way that it contributes  
to a sustainable and democratic transformation of our society? </li>
                <li>2.  What does a globally, economically, socially and ecologically  
just future look like in the digitalised world?  </li>
                <li>3. What can the tech community (bits) and the justice and environmental movement 
(bäume) learn from each other? And how can those communities get in touch with 
social-ecological pioneer companies, science and politics? </li>
                <li>4. How can we become politically active together?</li>
            </ul>

            <p>
            The Bits & Bäume 2022 participants want to interfere and stir things up politically. We openly 
stand up against anti-democratic trends and position ourselves for scientific-based, 
innovative, active and cross-movement approaches to solutions for sustainable digitalisation.
            </p>

            <p>With this Call for Participation, we call on people from civic organisations and progressive players 
within science, economy and politics to help shape the conference with their own contributions! 
We are looking forward to various diverse and creative topics and formats. Possible formats 
include discussion rounds, lightning talks, project presentations, campaign planning, (hands-on) 
workshops, activist info tables, installations, film screenings, hackathons, design thinking 
sessions, interviews, live podcasts, small group work, cultural formats, world cafés, craft 
workshops, art performances, design elements or anything else creative and innovative you can 
think of!</p>

            <p>In addition to all the stages and spaces of the broad call (1) we are also inviting initiatives and 
organisations to present themselves and their projects in our B&B-Forum to share knowledge 
and to engage with one another (2). Furthermore, we would like to welcome contributions from 
the business world with innovative approaches to the challenges described in this paper (3). 
Therefore there will be three different formats: contributions to the programme, B&B-Forum 
and Pitch & Thrive for Sustainability.</p>


            <p>
            
            Here you can find the  <a target="_blank" href="/downloads/call_for_participation_english.pdf">Call for Participation</a>.
            About this    <a target="_blank" href="https://pretalx.com/bitsundbaeume/cfp">formular</a> you can hand in a contribution 
            to the programme, the Forum or the Pitch & Thrive for Sustainability.


            </p>

