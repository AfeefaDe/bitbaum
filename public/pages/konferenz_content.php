                  Bastelwerkstätten, 
                Kunstperformances, Gestaltungselemente oder was euch sonst Kreatives und Innovatives einfällt!</p>

            <p>Neben den Bühnen und Räumen des breiten Calls (1) laden wir Initiativen und Organisationen auch ein, 
