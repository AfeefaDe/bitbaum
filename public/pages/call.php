  <p>Die Themen der Konferenz "Bits und Bäume" reichen von vernetzter Mobilität über den Rohstoffbedarf digitaler Geräte, Datensuffizienz und kooperativen Plattformökonomien bis hin zu smarten Energienetzen oder Monopolisierungstendenzen in der digitalen Ökonomie. Im Zentrum steht dabei immer die Schnittstelle von Nachhaltigkeit und Digitalisierung. Eine partizipative Konferenz, wie wir sie veranstalten wollen, lebt von den Ideen der Menschen, die sie mit uns gemeinsam gestalten werden. Daher sind die nachfolgend aufgeführten Leitfragen und Themen lediglich als Anregungen zu verstehen, welche Aspekte uns wichtig erscheinen. Wir freuen uns über Einreichungen in einem Call for Participation, der in Kürze mit allen dafür relevanten Infos an dieser Stelle erscheinen wird. Möchtest du das auf keinen Fall verpassen, kannst du dich hier bereits unverbindlich anmelden – und wir informieren dich, sobald der Call online ist.</p>

